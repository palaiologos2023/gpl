Η δομή array στην PHP είναι μια ισχυρή και ευέλικτη δομή δεδομένων <br>
που χρησιμοποιείται για την αποθήκευση πολλαπλών τιμών σε μια μεταβλητή.<br>
 Τα arrays στην PHP μπορούν να περιέχουν στοιχεία διαφόρων τύπων (π.χ., strings, αριθμούς, <br>
 ακόμα και άλλα arrays). Μπορούν να λειτουργούν τόσο ως <b> λίστες </b> <br>
  όσο και ως <b> συσχετισμένοι πίνακες- λεξικά </b> (associative arrays),
   γεγονός που τους καθιστά εξαιρετικά ευέλικτους.
   <p>	
<?php 

# δημιουργία  κενού πίνακα (array) 
# με χρήση των αγκυλών [] (προτιμώμενη μέθοδος από PHP 5.4 και μετά)
$array1 =[];
# δημιουργία πίνακα με στοιχεία 
$array2 = [10,23];
#Προσθήκη στοιχείου στο τέλος του πίνακα με [] 
$array1[] = 10;
# Με την συνάρτηση array_push() ή οποία  προσθέτει ένα ή περισσότερα στοιχεία 
# στο τέλος του πίνακα:
array_push($array1, 90);
array_push($array2, 35,23,67);
#Εκτύπωση όλα τα στοιχεία του πίνακα 
print_r($array1);
echo "<br>";
print_r($array2);
echo "<br>";
#Εκτύπωση ένα στοιχείο συγκεκριμένο 
echo " Το στοιχείο στην θέση 0 είναι το  $array1[0] <br>";
echo " Το στοιχείο στην θέση 1 είναι το  $array1[1] <br>";
$array1[1]=6789;
#Εκτύπωση σε θέση που δεν υπάρχει 
echo " Το στοιχείο στην θέση 4 είναι το  $array1[4] <br>";
# Προσθήκη στοιχείου σε συγκεκριμένη θέση 
$array1[2]=30;
$array1[10]=30;
$array1[]=130;
#Προσθήκη τιμή χαρακτήρες 
$array1[]="Καλημέρα ";
#Εκτύπωση στοιχεία του πίνακα ένα - ενα τιμές
echo "<br>";
foreach ($array1 as $value) {
	echo ($value);
	echo "<br>";
  }

  foreach ($array1 as $key => $value) {
    echo "Key: $key, Value: $value <br> ";
}

#Προσθήκη στοιχείου και θέση είναι χαρακτήρες
$array1["Monday"]="Δευτέρα ";
$array1[]= "Test123";
foreach ($array1 as $key => $value) {
    echo "Key: $key, Value: $value <br> ";
}






